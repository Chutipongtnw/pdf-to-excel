def clean_subject_name(text):
    if not text: return "N/A"
    
    # 1. ตัดส่วน "จำนวน" และ "หน่วยการเรียน" ทิ้งทันที (ลบทุกอย่างที่อยู่หลังคำว่า จ)
    # เราจะค้นหาจุดเริ่มต้นของคำว่า "จำนวน" "จํานวน" หรือตัวเลขหน่วยกิต แล้วตัดทิ้งทั้งหมด
    stop_words = ["จำนวน", "จํานวน", "หน่วย", "หนวย"]
    for word in stop_words:
        if word in text:
            text = text.split(word)[0]

    # 2. แก้ไขสระและวรรณยุกต์ที่ "ลอย" หรือเป็น "สี่เหลี่ยม" ด้วยรหัส Unicode ตรงๆ
    # แก้ไข 'ฟิสกิ ส์' 'ทัศนศลิป' 'นาฎศิลป' 'คณิตศาสตร'
    unicode_fixes = {
        '\uf701': 'ิ', '\uf702': 'ี', '\uf703': 'ึ', '\uf704': 'ื',
        '\uf705': '่', '\uf706': '้', '\uf70e': '์', '\uf710': '่',
        '\uf711': '้', '\uf714': '์', 'ศลิป': 'ศิลป์', 'หนวย': 'หน่วย',
        'ฟิสกิ': 'ฟิสิก', 'สิกส': 'สิกส์', 'ศาสตร': 'ศาสตร์', 'ผลติ': 'ผลิต',
        'ศกึ': 'ศึก', 'นาฎ': 'นาฏ', 'พิ่ม': 'เพิ่ม'
    }
    for wrong, right in unicode_fixes.items():
        text = text.replace(wrong, right)

    # 3. ลบอักขระขยะ Unicode ทั้งหมดที่ทำให้เกิดสี่เหลี่ยม
    text = re.sub(r'[\uf000-\uf0ff]', '', text)
    
    # 4. ลบช่องว่างส่วนเกิน
    text = " ".join(text.split())
    
    # 5. เช็คคำสุดท้าย ถ้าจบด้วย 'ศาสตร์' แล้วการันต์หาย ให้เติมคืน
    if text.endswith('คณิตศาสตร'): text += '์'
    if text.endswith('ฟิสิกส'): text += '์'
    if text.endswith('ศิลป'): text += '์'

    return text.strip()

